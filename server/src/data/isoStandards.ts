// Helper functions
export function getStandardByCode(code: string): ISOStandard | undefined {
  return isoStandardsEnhanced[code];
}

export function getAllClauses(code: string): ISOClause[] {
  return isoStandardsEnhanced[code]?.clauses || [];
}

export function getClauseById(standardCode: string, clauseId: string): ISOClause | undefined {
  return isoStandardsEnhanced[standardCode]?.clauses.find(c => c.id === clauseId);
}

export function getAllStandardCodes(): string[] {
  return Object.keys(isoStandardsEnhanced);
}

export function getStandardSummary() {
  return Object.values(isoStandardsEnhanced).map(s => ({
    code: s.code,
    name: s.name,
    fullName: s.fullName,
    version: s.version,
    totalClauses: s.clauses.length,
    categories: s.categories,
  }));
}
